package com.BasicsOfJava;

public class ForLoops {
    public static void main(String[] args) {

        // Use forLoops if you know the situation ahead

        for (int i = 0; i < 5; i++){
            System.out.println("Hello World " + i);
        }

        for (int j = 5; j > 0; j--){
            System.out.println("Hello World " + j);
        }
    }
}
